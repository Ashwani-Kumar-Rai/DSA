#include<iostream>
using namespace std;
int main(){
    int n;
    cout<<"Enter a number \n";
    cin>>n;

    if(n==0)
    cout<<"THE NUMBER IS ZERO \n";

    else if(n>0)
    cout<<"THE NUMBER IS POSITIVE \n";

    else
    cout<<"THE NUMBER IS NEGATIVE \n";

    return 0;
}