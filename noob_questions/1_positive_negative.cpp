#include<iostream>
using namespace std;
int main(){
    int num;
    cout<<"Enter the number\n";
    cin>>num;

    if(num==0)
    cout<<"\n The number is zero \n";

    else if(num>0)
    cout<<"\n The number is Positive \n";

    else
    cout<<"The number is Negative \n";

    return 0;
}



